export const THEMES: { [key: string]: ThemeDefinition } = allThemes.reduce((acc, theme) => {
  acc[theme.id] = theme;
  return acc;
}, {} as { [key: string]: ThemeDefinition });


export const themeList = Object.values(THEMES);

export const featuredThemeIds = [
  'warm-ember',
  'the-matrix',
  'luigis-mansion',
  'modern-minimal',
  'star-wars',
  'f-zero',
  'windows-95',
  'pastel-pop',
];

// New structure for the settings view
const featuredThemes = featuredThemeIds.map(id => THEMES[id]);
const nonFeaturedThemes = themeList.filter(theme => !featuredThemeIds.includes(theme.id));

export const groupedThemes: Record<string, ThemeDefinition[]> = nonFeaturedThemes.reduce((acc, theme) => {
  const category = theme.category || 'Uncategorized';
  if (!acc[category]) {
    acc[category] = [];
  }
  acc[category].push(theme);
  return acc;
}, {} as Record<string, ThemeDefinition[]>);

// Add the featured group
groupedThemes['Featured'] = featuredThemes;

// Alphabetize themes within each category, but not the hand-picked featured list
Object.keys(groupedThemes).forEach(category => {
    if (category !== 'Featured') {
      groupedThemes[category].sort((a, b) => a.name.localeCompare(b.name));
    }
});

